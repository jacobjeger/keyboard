    /** Skin tone modifiers */
    val skinTones = listOf(
        "",           // default yellow
        "\uD83C\uDFFB", // light
        "\uD83C\uDFFC", // medium-light
        "\uD83C\uDFFD", // medium
        "\uD83C\uDFFE", // medium-dark
        "\uD83C\uDFFF"  // dark
    )

    /** Apply skin tone to an emoji that supports it */
    fun applySkinTone(emoji: String, toneIndex: Int): String {
        if (toneIndex <= 0 || toneIndex >= skinTones.size) return emoji
        // Insert skin tone modifier after first code point
        val firstCodePoint = emoji.codePointAt(0)
        val charCount = Character.charCount(firstCodePoint)
        return emoji.substring(0, charCount) + skinTones[toneIndex] + emoji.substring(charCount)
    }

