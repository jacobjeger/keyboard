    /** Search emoji by name */
    fun search(query: String): List<Emoji> {
        if (query.isBlank()) return emptyList()
        val lower = query.lowercase()
        return allEmojis.filter { it.name.contains(lower) }
    }

    /** Get emoji by category */
    fun getByCategory(category: EmojiCategory): List<Emoji> {
        return allEmojis.filter { it.category == category }
    }
