    /**
     * Predict emoji for a given word.
     * @param word The typed word to match against keywords.
     * @param language The current language code (unused for now, keywords handle both).
     * @return Up to 3 emoji suggestions, filtered through kosher filter if available.
     */
    fun predict(word: String, language: String): List<String> {
        if (word.isBlank()) return emptyList()

        val key = word.lowercase().trim()
        val emojis = keywordMap[key] ?: return emptyList()

        val filtered = if (kosherFilter != null) {
            emojis.filter { !kosherFilter.isBlocked(it) }
        } else {
            emojis
        }

        return filtered.take(3)
    }
